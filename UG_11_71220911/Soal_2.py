print ("Pemeriksa Kelipatan 9")
bilangan = int (input("Masukkan Kelipatan 9 yang ingin diperiksa: "))

def kelipatan_sembilan(kelipatan_sembilan):
    kelipatan_sembilan = bilangan / 9
    return kelipatan_sembilan

if kelipatan_sembilan(kelipatan_sembilan) == 0:
    print ("Benar")

else: print ("Salah")
